BloomFilterSchema.methods.test = function (v) {
  var l = this.locations(v + "");
  for (var i = 0; i < this.hashFunctions; ++i) {
    var b = l[i];
    if ((this.buckets[Math.floor(b / 32)] & (1 << b % 32)) === 0) {
      return false;
    }
  }
  return true;
};
// Estimated cardinality.
BloomFilterSchema.methods.size = function () {
  var bits = 0;
  for (var i = 0, n = this.buckets.length; i < n; ++i)
    bits += popcnt(this.buckets[i]);
  return (-this.space * Math.log(1 - bits / this.space)) / this.hashFunctions;
};

// http://graphics.stanford.edu/~seander/bithacks.html#CountBitsSetParallel
function popcnt(v) {
  v -= (v >> 1) & 0x55555555;
  v = (v & 0x33333333) + ((v >> 2) & 0x33333333);
  return (((v + (v >> 4)) & 0xf0f0f0f) * 0x1010101) >> 24;
}

// Fowler/Noll/Vo hashing.
// Nonstandard variation: this function optionally takes a seed value that is incorporated
// into the offset basis. According to http://www.isthe.com/chongo/tech/comp/fnv/index.html
// "almost any offset_basis will serve so long as it is non-zero".
function fnv_1a(v, seed) {
  var a = 2166136261 ^ (seed || 0);
  for (var i = 0, n = v.length; i < n; ++i) {
    var c = v.charCodeAt(i),
      d = c & 0xff00;
    if (d) a = fnv_multiply(a ^ (d >> 8));
    a = fnv_multiply(a ^ (c & 0xff));
  }
  return fnv_mix(a);
}

// a * 16777619 mod 2**32
function fnv_multiply(a) {
  return a + (a << 1) + (a << 4) + (a << 7) + (a << 8) + (a << 24);
}

// See https://web.archive.org/web/20131019013225/http://home.comcast.net/~bretm/hash/6.html
function fnv_mix(a) {
  a += a << 13;
  a ^= a >>> 7;
  a += a << 3;
  a ^= a >>> 17;
  a += a << 5;
  return a & 0xffffffff;
}

