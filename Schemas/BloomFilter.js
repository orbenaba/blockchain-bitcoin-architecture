BloomFilterSchema.methods.locations = async function (v) {
  var a = fnv_1a(v),
    b = fnv_1a(v, 1576284489), // The seed value is chosen randomly
    x = a % this.space;
  for (var i = 0; i < this.hashFunctions; ++i) {
    this._locations[i] = x < 0 ? x + this.space : x;
    x = (x + b) % this.space;
  }
  return this._locations;
};

